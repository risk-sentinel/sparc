#!/usr/bin/env ruby
# frozen_string_literal: true

# Regenerates docs/dev/aws_labs_cdef_coverage.md from the two NIST mapping
# JSON files shipped with SPARC:
#
#   lib/data_mappings/aws_security_hub_to_nist.json  (AWS Sec Hub -> NIST rev5)
#   lib/data_mappings/mitre_aws_config_to_nist.json  (MITRE AWS Config -> NIST rev4)
#
# CDEF inventory is derived from the Sec Hub mapping's `service_slug`
# field (one CDEF per service group, matching the AWS Labs layout).
#
# Usage (from repo root):
#   bin/aws_labs_cdef_coverage_report.rb           # writes report to docs/dev/
#   bin/aws_labs_cdef_coverage_report.rb --stdout  # prints to stdout instead
#
# Re-run after refreshing either mapping JSON.

require "json"
require "set"

REPO_ROOT = File.expand_path("..", __dir__)
SEC_HUB_PATH = File.join(REPO_ROOT, "lib/data_mappings/aws_security_hub_to_nist.json")
MITRE_PATH   = File.join(REPO_ROOT, "lib/data_mappings/mitre_aws_config_to_nist.json")
OUTPUT_PATH  = File.join(REPO_ROOT, "docs/dev/aws_labs_cdef_coverage.md")

to_stdout = ARGV.include?("--stdout")

sec_hub = JSON.parse(File.read(SEC_HUB_PATH))
mitre   = JSON.parse(File.read(MITRE_PATH))

mitre_rules = mitre["mappings"].each_with_object({}) do |m, h|
  h[m["aws_config_rule_name"]] = (m["nist_oscal_ids"] || [])
end

by_service = sec_hub["mappings"].group_by { |m| m["service_slug"] }.sort.to_h

rows = by_service.map do |slug, controls|
  asff_nist = Set.new
  mitre_nist = Set.new
  unmapped_by_asff = []
  unmapped_by_mitre = []
  fully_unmapped = []

  controls.each do |c|
    sec_id = c["sec_hub_id"]
    asff_ids = c["nist_oscal_ids"] || []
    config_rule = c["aws_config_rule"]
    mitre_ids = config_rule && mitre_rules[config_rule] || []

    asff_nist.merge(asff_ids)
    mitre_nist.merge(mitre_ids)

    unmapped_by_asff << sec_id if asff_ids.empty?
    unmapped_by_mitre << sec_id if mitre_ids.empty?
    fully_unmapped << sec_id if asff_ids.empty? && mitre_ids.empty?
  end

  {
    slug: slug,
    total: controls.size,
    asff_nist: asff_nist,
    mitre_nist: mitre_nist,
    union_nist: asff_nist | mitre_nist,
    unmapped_by_asff: unmapped_by_asff,
    unmapped_by_mitre: unmapped_by_mitre,
    fully_unmapped: fully_unmapped
  }
end

global_nist = Set.new
rows.each { |r| global_nist.merge(r[:union_nist]) }

def fmt_list(ids, limit: nil)
  return "_(none)_" if ids.empty?
  shown = limit ? ids.first(limit) : ids
  text = shown.map { |i| "`#{i}`" }.join(", ")
  text += ", *(+#{ids.size - limit} more)*" if limit && ids.size > limit
  text
end

def sort_nist(ids)
  ids.sort_by do |id|
    fam, num, *rest = id.split(/[-.]/)
    [fam.to_s, num.to_i, rest.join(".")]
  end
end

out = String.new
out << "# AWS Labs CDEF — NIST 800-53 Coverage Report\n\n"
out << "_Auto-generated by `bin/aws_labs_cdef_coverage_report.rb`. Do not hand-edit._\n\n"
out << "## Sources\n\n"
out << "- **AWS Security Hub → NIST 800-53 rev5** — `lib/data_mappings/aws_security_hub_to_nist.json` "
out << "(version `#{sec_hub["version"]}`, #{sec_hub["total_entries"]} entries, source: `#{sec_hub["source"]}`)\n"
out << "- **MITRE AWS Config → NIST 800-53 rev4** — `lib/data_mappings/mitre_aws_config_to_nist.json` "
out << "(version `#{mitre["version"]}`, #{mitre["total_entries"]} entries, source: `#{mitre["source"]}`)\n\n"
out << "CDEF inventory is derived from the Sec Hub mapping's `service_slug` field "
out << "(one CDEF per AWS service group, matching the AWS Labs `oscal-content-for-aws-services` layout).\n\n"

out << "## Methodology\n\n"
out << "For each Sec Hub control (e.g., `IAM.1`):\n\n"
out << "- **ASFF mapping** — directly read from the Sec Hub JSON's `nist_oscal_ids` field (NIST rev5).\n"
out << "- **MITRE mapping** — joined via the Sec Hub control's `aws_config_rule` field against MITRE's "
out << "Config-rule → NIST rev4 map.\n"
out << "- **Fully unmapped** — Sec Hub controls where *both* sources are empty. These are the hand-curation candidates.\n\n"
out << "rev4 ↔ rev5 translation is **not applied** here — counts are reported per source as-is. "
out << "When the Rev 4 ↔ Rev 5 ControlMapping (#499) lands, the union column should grow.\n\n"

out << "## Per-CDEF coverage\n\n"
out << "| Service (slug) | AWS Labs controls | NIST controls (ASFF) | NIST controls (MITRE) | Unmapped by ASFF | Unmapped by MITRE | Fully unmapped |\n"
out << "|---|---:|---:|---:|---:|---:|---:|\n"

rows.each do |r|
  out << "| `#{r[:slug]}` "
  out << "| #{r[:total]} "
  out << "| #{r[:asff_nist].size} "
  out << "| #{r[:mitre_nist].size} "
  out << "| #{r[:unmapped_by_asff].size} "
  out << "| #{r[:unmapped_by_mitre].size} "
  out << "| #{r[:fully_unmapped].size} |\n"
end

out << "\n## Per-CDEF detail\n\n"
rows.each do |r|
  out << "### `#{r[:slug]}` (#{r[:total]} AWS Labs controls)\n\n"
  out << "- **NIST controls covered (union of ASFF + MITRE):** #{r[:union_nist].size}\n"
  out << "- **Unmapped by ASFF:** #{r[:unmapped_by_asff].size} — #{fmt_list(r[:unmapped_by_asff].sort, limit: 20)}\n"
  out << "- **Unmapped by MITRE:** #{r[:unmapped_by_mitre].size} — #{fmt_list(r[:unmapped_by_mitre].sort, limit: 20)}\n"
  out << "- **Fully unmapped (both sources empty):** #{r[:fully_unmapped].size} — #{fmt_list(r[:fully_unmapped].sort, limit: 20)}\n"
  out << "- **NIST controls (union):** #{fmt_list(sort_nist(r[:union_nist].to_a))}\n\n"
end

out << "## Summary\n\n"
total_controls = rows.sum { |r| r[:total] }
total_unmapped_both = rows.sum { |r| r[:fully_unmapped].size }
out << "- **CDEFs (service groups):** #{rows.size}\n"
out << "- **Total AWS Labs controls:** #{total_controls}\n"
out << "- **Unique NIST controls covered (across all CDEFs):** #{global_nist.size}\n"
out << "- **Fully unmapped AWS Labs controls (both sources empty):** #{total_unmapped_both} "
out << "(#{(total_unmapped_both * 100.0 / total_controls).round(1)}% of total)\n\n"

out << "### Unique NIST controls covered\n\n"
out << fmt_list(sort_nist(global_nist.to_a))
out << "\n"

if to_stdout
  puts out
else
  File.write(OUTPUT_PATH, out)
  puts "Wrote #{OUTPUT_PATH} (#{out.bytesize} bytes)"
end
